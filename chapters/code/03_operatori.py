# 1. Definisci le variabili
a = 15
b = 4

# 2. Operazioni aritmetiche base
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")

# 3. Divisione intera e resto
print(f"{a} // {b} = {a // b}")
print(f"{a} % {b} = {a % b}")

# 4. Confronto
print(f"{a} > {b} ? {a > b}")

# 5. Verifica se 'a' è pari
print(f"{a} è pari? {(a % 2) == 0}")

# 6. Assegnamento composto
punti = 50
punti += 25
print(f"Nuovo punteggio: {punti}")
